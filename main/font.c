void draw_text(const char* text, uint8_t col, uint8_t row, uint8_t *canvas){
	uint8_t curr_col = col;
	uint8_t curr_row = row;
	
	for(int i = 0; text[i] != '\0'; i++){
		if(curr_col >= MAX_COLS){
			curr_col = 0;
			curr_row++;
		}

		if(curr_row >= MAX_ROWS){
			break;
		}
		copy_char_aligned(font_vertical[(uint8_t)text[i]], curr_col, curr_row, canvas);
		curr_col++;
	}
}

size_t str_len(const char* c){
		const char* end = c;
		while(*end != '\0')++end;
		return end-c;
}

void copy_char_aligned(const uint8_t char_data[8], uint8_t col, uint8_t row, uint8_t* canvas){
	if(col >= MAX_COLS || row >= MAX_ROWS){
		return;
	}
	
	uint16_t canvas_index = (row * 128) + (col * 8);

	memcpy(&canvas[canvas_index], char_data, 8);

}

